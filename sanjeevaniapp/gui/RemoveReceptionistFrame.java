    private void loadReceptionitId() {
       try
       {
           List<String>recepList=ReceptionistDao.getAllReceptionistId();
           for(String recepId:recepList){
               ComboReceptionistrtId.addItem(recepId);
           }
           if(ComboReceptionistrtId.getItemCount()!=0)
                btnDelete.setEnabled(true);
       }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB in RemoveReceptionistFrame :"+ex.getMessage());
            ex.printStackTrace();
        }
    }
