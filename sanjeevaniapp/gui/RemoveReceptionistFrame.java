    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String RecepId=ComboReceptionistrtId.getSelectedItem().toString();
        try
        {
            boolean result=ReceptionistDao.deleteReceptionistById(RecepId);
            if(result)
            {
                JOptionPane.showMessageDialog(null, "Remove Receptionist successfully");
                ComboReceptionistrtId.removeItem(RecepId);
                if(ComboReceptionistrtId.getItemCount()==0)
                {
                    btnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null,"All Receptionists removed!");
                }
            }else{
                JOptionPane.showMessageDialog(null,"Deletion unsuccessful");
            }
        }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB In RemoveDoctorFrame:"+ex.getMessage());
             ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

