    private void loadDoctorsId() {
        try
        {
            List<String>docList=DoctorDao.getAllDoctorId();
            for(String docId:docList)
            {
                ComboDoctorId.addItem(docId);
            }
            if(ComboDoctorId.getItemCount()!=0)
                btnDelete.setEnabled(true);
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB in RemoveDoctorFrame :"+ex.getMessage());
            ex.printStackTrace();
        }
    }
