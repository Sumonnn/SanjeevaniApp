    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String docId=ComboDoctorId.getSelectedItem().toString();
        try
        {
            boolean result=DoctorDao.deleteDoctorById(docId);
            if(result)
            {
                JOptionPane.showMessageDialog(null,"Doctor Remove successfully");
                ComboDoctorId.removeItem(docId);
                if(ComboDoctorId.getItemCount()==0)
                {
                    btnDelete.setEnabled(false);
                    JOptionPane.showMessageDialog(null,"All Doctors removed!");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Deletion unsuccessful");
            }
        }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB In RemoveDoctorFrame:"+ex.getMessage());
             ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

