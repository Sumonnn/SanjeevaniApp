    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
       String PatientId=ComboPatientId.getSelectedItem().toString();
       try
       {
           boolean result=PatientDao.RemovePatient(PatientId);
           if(result)
           {
               JOptionPane.showMessageDialog(null,"Patient Delete Successfully");
               ComboPatientId.removeItem(PatientId);
               if(ComboPatientId.getItemCount()==0)
               {
                   btnDelete.setEnabled(false);
                   JOptionPane.showMessageDialog(null, "All Patient Deleteds!");
               }
           }else{
               JOptionPane.showMessageDialog(null, "Deletion Unsuccessful");
           }
       }catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB In RemovePatientFrame :"+ex.getMessage());
             ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

