    private void loadpatientId() {
       try
       {
           List<String> patientId=PatientDao.getAllPatientsId();
           for(String id:patientId)
           {
               ComboPatientId.addItem(id);
           }
           if(ComboPatientId.getItemCount()!=0)
               btnDelete.setEnabled(true);
           
       }
       catch(SQLException ex)
       {
                JOptionPane.showMessageDialog(null,"Error In DB in RemovePatientsFrame "+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }
