    private void loadEmpId() {
         try{
            List<String>empIdList=EmpDao.getAllEmployeeId();
            for(String id:empIdList){
                ComboEmpId.addItem(id);
            }
        }
       catch(SQLException ex)
       {
                JOptionPane.showMessageDialog(null,"Error In DB in RemoveEmployeeFrame "+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
    }
