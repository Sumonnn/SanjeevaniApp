    private void btnEmpDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmpDeleteActionPerformed
            try
            {
                String empId=ComboEmpId.getSelectedItem().toString();
                EmpPojo emp=EmpDao.getEmployeeDetails(empId);
                System.out.println(emp);
                 Boolean result=EmpDao.RemoveEmployee(emp);
                 if(result)
                 {
               JOptionPane.showMessageDialog(null, "Record Deleted successfully!");
              
               return;
                }
           JOptionPane.showMessageDialog(null, "Record not deleted. Try again");
  
          }
            catch(SQLException ex)
           {
           JOptionPane.showMessageDialog(null," Error In DB in RemoveEmployeeFrame "+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
         }
    }//GEN-LAST:event_btnEmpDeleteActionPerformed

